    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        String nombreMasco = txtNombre.getText();
        String raza = txtRaza.getText();
        String color = txtColor.getText();
        String observaciones = txtObservaciones.getText();
        String nomDueño = txtNomDueño.getText();
        String celDueño = txtCelDueño.getText();
        
        String alergico = (String) cmbAlergico.getSelectedItem();
        String atencion = (String) cmbAtencion.getSelectedItem();
        
        control.guardar(nombreMasco,raza,color,observaciones,nomDueño,celDueño,alergico,atencion);
        
        JOptionPane optionPane = new JOptionPane("Se guardo correctamente");
        optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        JDialog dialog = optionPane.createDialog("Guardado Exitoso");
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
        
    }//GEN-LAST:event_btnGuardarActionPerformed

